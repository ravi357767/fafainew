            <p>FAFAI has its house Journal titled “The FAFAI Journal” being published every quarter which carries research articles and recent developments in reference to the industry by eminent personalities and scientists from India and abroad. The Journal also covers the latest development at market place for the members in the industry. It is also the single and largest platform of advertising for our members to reach out to the right kind of customer base.</p>

            <p> The Association maintains a technical library which is a treasure of wide range of technical books related to Fragrance and Flavour industry, chemistry and essential oils. The books are available for study and reference to our members. We also have a Data Bank Library in Ahmedabad, Gujarat.</p>

