            <p>Fragrances and Flavours Association of India (FAFAI) is an apex body of 800 members in the field of Fragrances, Flavours, Aroma chemicals, Natural Oils and its ancillaries. The Association is instrumental in representing grievances of members to various government bodies.</p>

            <p>In 1949, a humble beginning by few traders of Fragrances &Flavours industry, with a view of promoting unity and fraternity amongst traders and dealers grew into a national Association to incorporate manufacturers and dealers in Fragrance and Flavour industry, Aroma Chemicals, Natural Essential Oils as well as related allied fields.</p>

